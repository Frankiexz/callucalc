user_input = Entry(root, textvariable=input_t, font=('arial', 20, 'bold'), width=50, bd=0)
user_input.pack(side="top", ipady=15, fill="both", expand=True)
user_input.focus()
input_t.set("Enter input in the format: x(operation)y")
user_input.icursor(len(input_t.get()))

def btn_click(item):
    global expression
    expression = expression + str(item)
    user_input.set(expression)
    
def bt_clear(): 
    global expression 
    expression = "" 
    user_input.set("")
    
